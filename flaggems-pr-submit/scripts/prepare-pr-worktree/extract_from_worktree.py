def extract_kernel(op, worktree, repo, dry_run):
    section("Step 1: Kernel 文件")
    module_name = get_module_name(op)
    src = os.path.join(worktree, f"src/flag_gems/ops/{module_name}.py")
    dst = os.path.join(repo, f"src/flag_gems/ops/{module_name}.py")

    if not os.path.isfile(src):
        # Inplace ops (trailing _) may have filename without the underscore
        op_stripped = module_name.rstrip("_")
        alt_src = os.path.join(worktree, f"src/flag_gems/ops/{op_stripped}.py")
        if op_stripped != module_name and os.path.isfile(alt_src):
            src = alt_src
            dst = os.path.join(repo, f"src/flag_gems/ops/{op_stripped}.py")
            warn(f"使用去下划线文件名: {op_stripped}.py")
        else:
            fatal(f"worktree kernel 不存在: {src}")

    with open(src) as f:
        content = f.read()

    if not content.startswith("# Generated by KernelGen"):
        content = KERNELGEN_HEADER + content
    if not content.endswith("\n"):
        content += "\n"

    if dry_run:
        ok(f"[DRY] 将写入 {dst} ({len(content.splitlines())} 行)")
    else:
        with open(dst, "w") as f:
            f.write(content)
        ok(f"已复制: {dst}")


