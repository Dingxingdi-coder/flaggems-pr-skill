def find_test_function(op, worktree):
    """Find and extract test function(s) for the operator from worktree.

    Search priority:
    1. test_accuracy_<op_id> — most common pattern
    2. test_accuracy_<op_name> — underscore-prefixed variant
    3. test_<op_id> — no accuracy prefix (reflection_pad3d, embedding etc.)
    4. Any function decorated with @pytest.mark.<op_id>
    """
    tests_dir = os.path.join(worktree, "tests")
    op_id = get_op_id(op)

    candidates = [
        f"test_accuracy_{op_id}",
        f"test_accuracy_{op}" if op.startswith("_") else None,
        f"test_{op_id}",
    ]
    candidates = [c for c in candidates if c]

    for target_func in candidates:
        for fname in os.listdir(tests_dir):
            if not fname.endswith(".py"):
                continue
            fpath = os.path.join(tests_dir, fname)
            with open(fpath) as f:
                content = f.read()
            if f"def {target_func}" in content:
                return fpath, content, target_func

    # Fallback: find by @pytest.mark.<op_id>
    mark_pattern = f"@pytest.mark.{op_id}"
    for fname in os.listdir(tests_dir):
        if not fname.endswith(".py"):
            continue
        fpath = os.path.join(tests_dir, fname)
        with open(fpath) as f:
            content = f.read()
        if mark_pattern in content:
            # Find the first test function after the mark
            match = re.search(rf"{re.escape(mark_pattern)}.*?def\s+(test_\w+)", content, re.DOTALL)
            if match:
                return fpath, content, match.group(1)

    return None, None, None


def find_all_related_test_functions(content, op_id, primary_func_name):
    """Find all test functions related to the operator in the same file.

    Looks for functions decorated with @pytest.mark.<op_id>.
    """
    func_names = []
    mark = f"@pytest.mark.{op_id}"

    # Use regex to find all def lines preceded (somewhere above) by the mark
    # Pattern: @pytest.mark.<op_id> followed eventually by def test_xxx
    for m in re.finditer(
        rf"^{re.escape(mark)}\s*\n(?:.*\n)*?^\s*def\s+(test_\w+)",
        content, re.MULTILINE
    ):
        fn = m.group(1)
        if fn not in func_names:
            func_names.append(fn)

    if not func_names and primary_func_name:
        func_names = [primary_func_name]

    return func_names


def extract_function_block(content, func_name):
    """Extract a function and its decorators from source code."""
    try:
        tree = ast.parse(content)
        for node in ast.walk(tree):
            if not isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef, ast.ClassDef)):
                continue
            if node.name != func_name:
                continue
            start = min([d.lineno for d in node.decorator_list] + [node.lineno]) - 1
            end = getattr(node, "end_lineno", None)
            if end is not None:
                return "\n".join(content.split("\n")[start:end])
    except SyntaxError:
        pass

    lines = content.split("\n")
    func_start = None
    decorator_start = None
    func_end = None

    for i, line in enumerate(lines):
        if f"def {func_name}" in line:
            func_start = i
            # Walk backwards to find decorators
            decorator_start = i
            j = i - 1
            while j >= 0:
                stripped = lines[j].strip()
                if stripped.startswith("@") or stripped == "":
                    if stripped.startswith("@"):
                        decorator_start = j
                    j -= 1
                else:
                    break
            break

    if func_start is None:
        return None

    # Find function end (next def/class at same or lower indent, or EOF)
    indent = len(lines[func_start]) - len(lines[func_start].lstrip())
    for i in range(func_start + 1, len(lines)):
        line = lines[i]
        if line.strip() == "":
            continue
        line_indent = len(line) - len(line.lstrip())
        if line_indent <= indent and (line.strip().startswith("def ") or
                                       line.strip().startswith("class ") or
                                       line.strip().startswith("@")):
            # Check if decorator belongs to this function
            if i > func_start + 1:
                func_end = i
                break
    if func_end is None:
        func_end = len(lines)

    # Trim trailing blank lines
    while func_end > decorator_start and lines[func_end - 1].strip() == "":
        func_end -= 1

    return "\n".join(lines[decorator_start:func_end])


def collect_top_level_defs(content):
    """Return top-level helper functions/classes keyed by name."""
    try:
        tree = ast.parse(content)
    except SyntaxError:
        return {}

    definitions = {}
    for node in tree.body:
        if isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef, ast.ClassDef)):
            block = extract_function_block(content, node.name)
            if block:
                definitions[node.name] = block
    return definitions


def collect_used_names(block):
    """Collect loaded names from a source block."""
    try:
        tree = ast.parse(block)
    except SyntaxError:
        return set()

    used = set()
    for node in ast.walk(tree):
        if isinstance(node, ast.Name) and isinstance(node.ctx, ast.Load):
            used.add(node.id)
    return used


def prepend_local_dependencies(content, blocks, exclude_names):
    """Prepend top-level helpers referenced by selected blocks."""
    definitions = collect_top_level_defs(content)
    selected_names = set(exclude_names)
    used = set()
    for block in blocks:
        used.update(collect_used_names(block))

    dep_blocks = []
    while True:
        added = False
        for name, block in definitions.items():
            if name in selected_names or name not in used:
                continue
            dep_blocks.append(block)
            selected_names.add(name)
            used.update(collect_used_names(block))
            added = True
        if not added:
            break

    return dep_blocks + list(blocks)


def convert_test_imports(func_block, op):
    """Convert direct accuracy_utils imports to `utils.` prefix style."""
    op_id = get_op_id(op)
    result = func_block

    # Replace function name: test_accuracy_<op> → test_<op>
    result = re.sub(rf"def test_accuracy_{re.escape(op_id)}\b",
                    f"def test_{op_id}", result)
    result = re.sub(r"\bdef test_accuracy_", "def test_", result)
    if op.startswith("_"):
        result = re.sub(rf"def test_accuracy_{re.escape(op)}\b",
                        f"def test_{op_id}", result)
        if op_id != op.lstrip("_"):
            def _replace_prefixed_func_name(match):
                suffix = match.group("suffix")
                suffix = f"_{suffix}" if suffix else ""
                return f"def test_{op_id}{suffix}"

            result = re.sub(
                rf"def test_accuracy_{re.escape(op)}(?P<suffix>\w*)\b",
                _replace_prefixed_func_name,
                result,
            )

    # Remove marks that are only meaningful in the monolithic worktree file.
    for mark in STRIP_TEST_MARKS:
        result = re.sub(rf"(?m)^@pytest\.mark\.{re.escape(mark)}\n", "", result)

    # Replace bare symbols with utils. prefix
    for sym in UTILS_SYMBOLS:
        # Only replace bare occurrences (not already prefixed)
        result = re.sub(rf"(?<!\.)(?<!\w){re.escape(sym)}\b", f"utils.{sym}", result)
    result = re.sub(r"(?<!utils\.)(?<!\w)init_seed\b", "utils.init_seed", result)

    # Replace device reference
    result = result.replace("device=device", "device=flag_gems.device")


    return result


def extract_test(op, worktree, repo, dry_run):
    section("Step 2: Test 文件")
    op_id = get_op_id(op)
    dst = os.path.join(repo, f"tests/test_{op_id}.py")

    fpath, content, func_name = find_test_function(op, worktree)
    if not fpath:
        fatal(f"worktree 中未找到 test_accuracy_{op_id} 或 test_{op_id} 函数")

    ok(f"找到测试: {os.path.basename(fpath)}:{func_name}")

    # Find all related test functions (including variants like _list_padding, _empty_padding)
    all_funcs = find_all_related_test_functions(content, op_id, func_name)
    ok(f"找到 {len(all_funcs)} 个相关测试函数: {', '.join(all_funcs)}")

    blocks = []
    for fn in all_funcs:
        block = extract_function_block(content, fn)
        if block:
            block = convert_test_imports(block, op)
            blocks.append(block)

    if not blocks:
        fatal(f"无法提取函数 {func_name}")

    blocks = prepend_local_dependencies(content, blocks, all_funcs)
    blocks = [convert_test_imports(block, op) for block in blocks]
    combined_blocks = "\n\n\n".join(blocks)

    extra_imports = ""
    if re.search(r"\brandom\.", combined_blocks):
        extra_imports += "import random\n"
    if re.search(r"\bmath\.", combined_blocks):
        extra_imports += "import math\n"
    if re.search(r"\bos\.", combined_blocks):
        extra_imports += "import os\n"
    if re.search(r"\bAny\b|\bList\b|\bOptional\b|\bTuple\b", combined_blocks):
        typing_names = [
            name
            for name in ("Any", "List", "Optional", "Tuple")
            if re.search(rf"\b{name}\b", combined_blocks)
        ]
        extra_imports += f"from typing import {', '.join(typing_names)}\n"

    test_content = (
        f"{extra_imports}"
        "import pytest\n"
        "import torch\n"
        "\n"
        "import flag_gems\n"
        "\n"
        "from . import accuracy_utils as utils\n"
        "\n"
        "\n"
        f"{combined_blocks}\n"
    )

    if dry_run:
        ok(f"[DRY] 将写入 {dst}")
        print(f"    预览:\n{test_content[:500]}")
    else:
        with open(dst, "w") as f:
            f.write(test_content)
        ok(f"已生成: {dst}")


