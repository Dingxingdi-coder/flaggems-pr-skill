def insert_ops_init(op, worktree, repo, dry_run):
    section("Step 4: ops/__init__.py")
    module_name = get_module_name(op)
    wt_init = os.path.join(worktree, "src/flag_gems/ops/__init__.py")
    repo_init = os.path.join(repo, "src/flag_gems/ops/__init__.py")

    # Extract import line and __all__ entries from worktree
    with open(wt_init) as f:
        wt_content = f.read()

    import_pattern = rf"^from flag_gems\.ops\.{re.escape(module_name)} import\b"
    import_line = None
    import_block = None
    wt_lines = wt_content.split("\n")
    for i, line in enumerate(wt_lines):
        if re.match(import_pattern, line):
            import_line = line
            # Check if multi-line import (ends with open paren)
            if line.rstrip().endswith("("):
                # Collect until closing paren
                block_lines = [line]
                for j in range(i + 1, len(wt_lines)):
                    block_lines.append(wt_lines[j])
                    if ")" in wt_lines[j]:
                        break
                import_block = "\n".join(block_lines)
            else:
                import_block = line
            break

    if not import_line:
        fatal(f"worktree ops/__init__.py 中未找到 {op} 的 import")

    # Extract __all__ names for this op (from the full block)
    all_names_match = re.search(
        rf'from flag_gems\.ops\.{re.escape(module_name)} import (.+)',
        import_block,
        re.DOTALL,
    )
    if all_names_match:
        all_names_raw = all_names_match.group(1)
        # Remove parens, strip whitespace
        all_names_raw = all_names_raw.replace("(", "").replace(")", "").replace("\n", " ")
        names = [n.strip().rstrip(",") for n in all_names_raw.split(",") if n.strip()]
    else:
        fatal(f"无法从 import 块提取导出名")
    ok(f"import 块: {import_block}")
    ok(f"导出名: {names}")

    # Read repo file
    with open(repo_init) as f:
        repo_content = f.read()

    # Insert import line in alphabetical order
    repo_lines = repo_content.split("\n")
    import_section_end = 0
    existing_imports = []
    for i, line in enumerate(repo_lines):
        if line.startswith("from flag_gems.ops."):
            existing_imports.append((i, line))
            import_section_end = i

    # Check if already exists
    if any(import_line in line for _, line in existing_imports):
        ok("import 已存在，跳过")
        all_end_adjust = 0
    else:
        # Find insertion point by alphabetical order
        insert_idx = import_section_end + 1
        for i, line in existing_imports:
            match = re.match(r"from flag_gems\.ops\.(\w+) import", line)
            if match:
                existing_mod = match.group(1)
                sort_key_existing = (not existing_mod.startswith("_"), existing_mod)
                sort_key_new = (not module_name.startswith("_"), module_name)
                if sort_key_new < sort_key_existing:
                    insert_idx = i
                    break
                insert_idx = i + 1

        # Insert multi-line import block (each line separately)
        block_lines = import_block.split("\n")
        for j, bline in enumerate(block_lines):
            repo_lines.insert(insert_idx + j, bline)
        ok(f"import 插入位置: 行 {insert_idx + 1} ({len(block_lines)} 行)")
        all_end_adjust = len(block_lines)  # __all__ end shifts by this much

    # Track how many lines were added for __all__ calculation

    # Insert into __all__
    # Find __all__ block
    all_start = None
    all_end = None
    for i, line in enumerate(repo_lines):
        if "__all__" in line and "=" in line:
            all_start = i
        if all_start and line.strip() == "]":
            all_end = i
            break

    if all_start and all_end:
        # Extract current __all__ entries
        all_entries = []
        for i in range(all_start, all_end + 1):
            matches = re.findall(r'"(\w+)"', repo_lines[i])
            all_entries.extend(matches)

        for name in names:
            if name in all_entries:
                ok(f'__all__ 已包含 "{name}"')
                continue

            # Find insertion point
            insert_idx = all_end
            for i in range(all_start + 1, all_end):
                match = re.search(r'"(\w+)"', repo_lines[i])
                if match:
                    existing = match.group(1)
                    if name < existing:
                        insert_idx = i
                        break
                    insert_idx = i + 1

            entry = f'    "{name}",'
            repo_lines.insert(insert_idx, entry)
            all_end += 1
            ok(f'__all__ 插入 "{name}" 在行 {insert_idx + 1}')

    new_content = "\n".join(repo_lines)

    if dry_run:
        ok("[DRY] ops/__init__.py 将被更新")
    else:
        with open(repo_init, "w") as f:
            f.write(new_content)
        ok("ops/__init__.py 已更新")


