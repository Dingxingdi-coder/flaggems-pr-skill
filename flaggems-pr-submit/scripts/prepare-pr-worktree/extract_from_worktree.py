def find_benchmark_code(op, worktree):
    """Find benchmark code for the operator in worktree."""
    op_id = get_op_id(op)
    bench_dir = os.path.join(worktree, "benchmark")

    for fname in os.listdir(bench_dir):
        if not fname.endswith(".py"):
            continue
        fpath = os.path.join(bench_dir, fname)
        with open(fpath) as f:
            content = f.read()
        if (f"mark.{op_id}" in content or f'op_name="{op_id}"' in content
                or f'"{op_id}"' in content
                or f'op_name="{op}"' in content or f'mark.{op_id.rstrip("_")}' in content):
            return fpath, content

    return None, None


def extract_benchmark_block(content, op, source_file):
    """Extract benchmark related code (class, input_fn, test function)."""
    op_id = get_op_id(op)
    lines = content.split("\n")
    blocks = []

    # Strategy 1: Find standalone class + test function (e.g., RandintBenchmark)
    class_pattern = re.compile(rf"class \w*{re.escape(op_id)}\w*Benchmark", re.IGNORECASE)
    for i, line in enumerate(lines):
        if class_pattern.search(line):
            cls_name = line.split("class ")[1].split("(")[0].split(":")[0]
            block = extract_function_block(content, cls_name)
            if block:
                blocks.append(("class", block))

    # Strategy 2: Find input_fn for this op
    input_fn_names = [
        f"{op_id}_input_fn", f"_{op_id}_input_fn", f"_input_fn",
    ]
    for fn_name in input_fn_names:
        for i, line in enumerate(lines):
            if f"def {fn_name}" in line:
                block = extract_function_block(content, fn_name)
                if block:
                    blocks.append(("input_fn", block))
                break

    # Strategy 3: Find standalone test function (e.g., test_perf_randint)
    test_patterns = [
        rf"def test_(?:perf_)?{re.escape(op_id)}\b",
        rf"def test_{re.escape(op_id)}\b",
        rf"def test_{re.escape(op)}\b",
        rf"def test_{re.escape(op)}_perf\b",
    ]
    for pat in test_patterns:
        for i, line in enumerate(lines):
            if re.search(pat, line):
                fname = re.search(r"def (\w+)", line).group(1)
                block = extract_function_block(content, fname)
                if block:
                    blocks.append(("test", block))
                break
        if any(t == "test" for t, _ in blocks):
            break

    # Strategy 4: op in a parametrized list → generate standalone benchmark
    if not any(t == "test" for t, _ in blocks):
        list_entry = _find_list_entry(lines, op_id, source_file)
        if list_entry:
            blocks.append(("list_entry", list_entry))

    test_text = "\n".join(b for t, b in blocks if t == "test" and isinstance(b, str))
    if test_text:
        existing_classes = {
            re.search(r"class\s+(\w+)", b).group(1)
            for t, b in blocks
            if t == "class" and isinstance(b, str) and re.search(r"class\s+(\w+)", b)
        }
        for cls_name in sorted(set(re.findall(r"\b([A-Z]\w*Benchmark)\s*\(", test_text))):
            if cls_name in existing_classes:
                continue
            block = extract_function_block(content, cls_name)
            if block and not block.splitlines()[0].startswith((" ", "\t")):
                blocks.insert(0, ("class", block))
                existing_classes.add(cls_name)

        existing_funcs = {
            re.search(r"def\s+(\w+)", b).group(1)
            for t, b in blocks
            if t == "input_fn" and isinstance(b, str) and re.search(r"def\s+(\w+)", b)
        }
        for fn_name in sorted(set(re.findall(r"\b(\w+_input_fn)\s*\(", test_text))):
            if fn_name in existing_funcs:
                continue
            block = extract_function_block(content, fn_name)
            if block and not block.splitlines()[0].startswith((" ", "\t")):
                insert_idx = 1 if blocks and blocks[0][0] == "class" else 0
                blocks.insert(insert_idx, ("input_fn", block))
                existing_funcs.add(fn_name)

        selected_names = set(existing_classes) | existing_funcs
        selected_names.update(
            re.search(r"def\s+(\w+)", b).group(1)
            for t, b in blocks
            if t == "test" and isinstance(b, str) and re.search(r"def\s+(\w+)", b)
        )
        selected = [b for _, b in blocks if isinstance(b, str)]
        with_deps = prepend_local_dependencies(content, selected, selected_names)
        if len(with_deps) > len(selected):
            existing_text = "\n\n".join(selected)
            for dep in with_deps[: len(with_deps) - len(selected)]:
                if dep in existing_text:
                    continue
                dep_type = "class" if dep.lstrip().startswith("class ") else "input_fn"
                insert_idx = 1 if blocks and blocks[0][0] == "class" else 0
                blocks.insert(insert_idx, (dep_type, dep))

    return blocks


def _find_list_entry(lines, op_id, source_file):
    """Find op in a parametrized operations list and return benchmark info dict."""
    basename = os.path.basename(source_file) if source_file else ""

    for i, line in enumerate(lines):
        stripped = line.strip()
        if f'"{op_id}"' not in stripped:
            continue

        tuple_text = _collect_tuple_text(lines, i)
        parsed = _parse_tuple_entry(tuple_text, op_id, basename) if tuple_text else None
        if parsed:
            parsed["is_inplace"] = _infer_is_inplace_context(lines, i)
            parsed["comments"] = _collect_preceding_comments(lines, i)
            return parsed

        # Parse single-line entries:
        #   ("op_name", torch.op, [dtypes])
        #   ("op_name", lambda a, b: a.__and__(b), INT_DTYPES + BOOL_DTYPES)
        prefix_match = re.match(rf'\(\s*"{re.escape(op_id)}"\s*,\s*(.+?)\s*\),?\s*$', stripped)
        if not prefix_match:
            continue

        args_part = prefix_match.group(1)
        if "," in args_part:
            torch_op, third_arg = [part.strip().rstrip(",") for part in args_part.rsplit(",", 1)]
        else:
            torch_op, third_arg = args_part.strip(), None

        # Determine benchmark type from file name
        bench_type = _classify_bench_type(basename)

        # Determine dtypes
        dtypes = _resolve_dtypes(third_arg)

        return {
            "type": bench_type,
            "torch_op": torch_op,
            "dtypes": dtypes,
            "op_id": op_id,
            "is_inplace": _infer_is_inplace_context(lines, i),
            "comments": _collect_preceding_comments(lines, i),
        }

    # Fallback: handle multi-line tuples where "op_name" and "torch.op" are on different lines
    for i, line in enumerate(lines):
        stripped = line.strip()
        if f'"{op_id}"' not in stripped:
            continue

        # Look for the enclosing tuple: join from "(" to matching ")"
        start = i
        while start >= 0 and "(" not in lines[start]:
            start -= 1
        end = i
        while end < len(lines) and ")" not in lines[end]:
            end += 1

        if start < 0 or end >= len(lines):
            continue

        joined = " ".join(lines[start:end + 1])
        # Now try to match the joined multi-line tuple
        match = re.search(
            rf'\(\s*"{re.escape(op_id)}"\s*,\s*([\w.\s,]+?)\s*\)',
            joined
        )
        if not match:
            continue

        args_part = match.group(1).strip().rstrip(",")
        # Split into parts: torch_op and optional dtypes/input_fn
        parts = [p.strip().rstrip(",") for p in args_part.split(",")]
        torch_op = None
        dtypes = None

        for p in parts:
            if re.match(r'^[\w.]+$', p) and "torch" in p:
                torch_op = p
            elif p.startswith("[") or re.match(r'^[A-Z_]+$', p):
                dtypes = _resolve_dtypes(p)
                break  # last meaningful arg after torch_op

        if not torch_op:
            continue

        bench_type = _classify_bench_type(basename)

        return {
            "type": bench_type,
            "torch_op": torch_op,
            "dtypes": dtypes,
            "op_id": op_id,
            "is_inplace": _infer_is_inplace_context(lines, i),
            "comments": _collect_preceding_comments(lines, i),
        }

    return None


def _collect_tuple_text(lines, entry_line):
    """Collect the complete tuple text for an op list entry."""
    start = entry_line
    while start >= 0 and not lines[start].lstrip().startswith("("):
        start -= 1
    if start < 0:
        return None

    collected = []
    depth = 0
    seen_open = False
    for j in range(start, len(lines)):
        part = lines[j].split("#", 1)[0]
        collected.append(lines[j].strip())
        depth += part.count("(") - part.count(")")
        seen_open = seen_open or "(" in part
        if seen_open and depth <= 0:
            break

    if not collected:
        return None
    return " ".join(collected).strip()


def _parse_tuple_entry(tuple_text, op_id, source_file):
    """Parse a parametrized benchmark tuple without splitting lambda commas."""
    expr = tuple_text.strip().rstrip(",")
    try:
        parsed = ast.parse(expr, mode="eval")
    except SyntaxError:
        return None

    node = parsed.body
    if not isinstance(node, ast.Tuple) or len(node.elts) < 2:
        return None
    name_node = node.elts[0]
    if not isinstance(name_node, ast.Constant) or name_node.value != op_id:
        return None

    torch_op = ast.get_source_segment(expr, node.elts[1]) or ast.unparse(node.elts[1])
    dtypes = None
    if len(node.elts) >= 3:
        dtypes = ast.get_source_segment(expr, node.elts[2]) or ast.unparse(node.elts[2])

    return {
        "type": _classify_bench_type(source_file),
        "torch_op": torch_op,
        "dtypes": _resolve_dtypes(dtypes),
        "op_id": op_id,
    }


def _infer_is_inplace_context(lines, entry_line):
    """Infer whether a parametrized entry belongs to an inplace benchmark."""
    for line in lines[entry_line: min(len(lines), entry_line + 100)]:
        stripped = line.strip()
        if stripped.startswith("@pytest.mark.parametrize") and line != lines[entry_line]:
            break
        if "is_inplace=True" in stripped:
            return True
    return False


def _collect_preceding_comments(lines, entry_line):
    comments = []
    j = entry_line - 1
    while j >= 0:
        stripped = lines[j].strip()
        if stripped.startswith("#"):
            comments.insert(0, stripped)
            j -= 1
            continue
        if stripped == "" and comments:
            j -= 1
            continue
        break
    return comments


def _classify_bench_type(basename):
    if "unary_pointwise" in basename:
        return "unary"
    elif "binary_pointwise" in basename:
        return "binary"
    return "generic"


def _resolve_dtypes(third_arg):
    if third_arg and third_arg.startswith("["):
        return third_arg
    if third_arg and "_DTYPES" in third_arg:
        return third_arg
    return None


def _prefix_consts_dtypes(expr):
    """Prefix bare benchmark dtype constants with consts."""
    return re.sub(r"(?<!consts\.)(?<!\w)([A-Z_]+_DTYPES)\b", r"consts.\1", expr)


def _prefix_benchmark_utils(text):
    """Prefix bare benchmark utility calls after switching to package imports."""
    return re.sub(
        r"(?<!utils\.)(?<!\w)generate_tensor_input\b",
        "utils.generate_tensor_input",
        text,
    )


def _normalize_benchmark_op_name(text, op_id):
    text = re.sub(
        rf"op_name\s*=\s*f[\"']{re.escape(op_id)}_[^\"']*[\"']",
        f'op_name="{op_id}"',
        text,
    )
    return re.sub(
        rf"op_name\s*=\s*[\"']{re.escape(op_id)}_[^\"']*[\"']",
        f'op_name="{op_id}"',
        text,
    )


def _format_torch_op_kwarg(torch_op):
    line = f"        torch_op={torch_op},\n"
    if len(line.rstrip("\n")) <= 120:
        return line

    if (
        torch_op.startswith("lambda a, b: torch.float_power(")
        and ").to(a.dtype)" in torch_op
    ):
        return (
            "        torch_op=(\n"
            "            lambda a, b: torch.float_power(\n"
            "                a.to(torch.float64), b.to(torch.float64)\n"
            "            ).to(a.dtype)\n"
            "        ),\n"
        )

    return line


def generate_benchmark_file(blocks, op, bench_source_file):
    """Generate independent benchmark file from extracted blocks."""
    op_id = get_op_id(op)

    has_class = any(t == "class" for t, _ in blocks)
    has_test = any(t == "test" for t, _ in blocks)
    list_entry = next((b for t, b in blocks if t == "list_entry"), None)

    # Case A: standalone class + input_fn + test function → combine them
    if has_class or has_test:
        if has_test:
            blocks = [
                (btype, block)
                for btype, block in blocks
                if not (
                    btype == "input_fn"
                    and isinstance(block, str)
                    and block.splitlines()
                    and block.splitlines()[0].startswith((" ", "\t"))
                )
            ]
        all_block_text = "\n".join(b for _, b in blocks if isinstance(b, str))
        needs_consts = ("consts." in all_block_text or "FLOAT_DTYPES" in all_block_text
                        or "INT_DTYPES" in all_block_text)
        needs_torch = "torch." in all_block_text
        needs_flag_gems = "flag_gems" in all_block_text
        needs_utils = "generate_tensor_input" in all_block_text or "utils." in all_block_text

        header = "import pytest\n"
        if needs_torch:
            header += "import torch\n"
        if needs_flag_gems:
            header += "\nimport flag_gems\n"
        header += "\nfrom . import base"
        if needs_consts:
            header += ", consts"
        if needs_utils:
            header += ", utils"
        header += "\n"

        parts = [header]
        for btype, block in blocks:
            if not isinstance(block, str):
                continue
            if btype == "test":
                block = re.sub(rf"def test_perf_{re.escape(op_id)}\b",
                               f"def test_{op_id}", block)
            parts.append(f"\n\n{block}")

        content = "\n".join(parts)

        # Add base. prefix to benchmark classes if not already prefixed
        for cls in ["GenericBenchmark", "GenericBenchmarkExcluse1D",
                     "GenericBenchmarkExcluse3D", "GenericBenchmark2DOnly",
                     "GenericBenchmark4DOnly", "UnaryPointwiseBenchmark",
                     "BinaryPointwiseBenchmark", "UnaryReductionBenchmark",
                     "BlasBenchmark", "Benchmark"]:
            content = re.sub(rf"(?<!base\.)(?<!\w){cls}\(", f"base.{cls}(", content)
            content = re.sub(rf"(?<!base\.)(?<!\w){cls}\)", f"base.{cls})", content)
        content = content.replace("base.base.", "base.")

        # Add consts. prefix
        for const in ["FLOAT_DTYPES", "INT_DTYPES", "EXTRA_INT_DTYPES"]:
            content = re.sub(rf"(?<!consts\.)(?<!\w){const}\b", f"consts.{const}", content)
        content = content.replace("consts.consts.", "consts.")
        content = _prefix_benchmark_utils(content)
        content = _normalize_benchmark_op_name(content, op_id)
        content = re.sub(r"(?<!base\.)(?<!\w)vendor_name\b", "base.vendor_name", content)
        content = re.sub(r"(?<!base\.)(?<!\w)Config\b", "base.Config", content)
        content = re.sub(r"(?<!consts\.)(?<!\w)BenchLevel\b", "consts.BenchLevel", content)
        content = content.replace(
            "        dtypes=[torch.float32] if base.vendor_name == \"mthreads\" else consts.FLOAT_DTYPES,\n",
            "        # MThreads supports only float32 here; NVIDIA uses consts.FLOAT_DTYPES.\n"
            "        dtypes=[torch.float32] if base.vendor_name == \"mthreads\" else consts.FLOAT_DTYPES,\n",
        )

    # Case B: list entry → generate standard benchmark
    elif list_entry:
        info = list_entry
        torch_op = info["torch_op"]
        dtypes_str = info.get("dtypes")

        if info["type"] == "unary":
            cls = "base.UnaryPointwiseBenchmark"
        elif info["type"] == "binary":
            cls = "base.BinaryPointwiseBenchmark"
        else:
            cls = "base.GenericBenchmark"

        # Build dtypes argument
        if dtypes_str:
            # Bare DTYPES names need consts. prefix
            if re.match(r'^[A-Z_]+_DTYPES$', dtypes_str):
                dtypes_arg = f"dtypes=consts.{dtypes_str}"
                needs_consts = True
            elif dtypes_str.startswith("["):
                dtypes_arg = f"dtypes={dtypes_str}"
                needs_consts = False
            else:
                dtypes_expr = _prefix_consts_dtypes(dtypes_str)
                dtypes_arg = f"dtypes={dtypes_expr}"
                needs_consts = "consts." in dtypes_expr
        else:
            dtypes_arg = "dtypes=consts.FLOAT_DTYPES"
            needs_consts = True

        needs_torch = "torch." in torch_op
        header = "import pytest\n"
        if needs_torch:
            header += "import torch\n"
        header += "\nfrom . import base"
        if needs_consts:
            header += ", consts"
        header += "\n"

        content = f"""{header}


@pytest.mark.{op_id}
def test_{op_id}():
    bench = {cls}(
        op_name="{op_id}",
"""
        content += _format_torch_op_kwarg(torch_op)
        for comment in info.get("comments", []):
            content += f"        {comment}\n"
        content += f"        {dtypes_arg},\n"
        if info.get("is_inplace"):
            content += "        is_inplace=True,\n"
        content += """    )
    bench.run()
"""
    else:
        return None

    if not content.endswith("\n"):
        content += "\n"
    return content


def extract_benchmark(op, worktree, repo, dry_run):
    section("Step 3: Benchmark 文件")
    op_id = get_op_id(op)
